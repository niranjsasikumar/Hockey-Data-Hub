  `tiesInUse` tinyint DEFAULT NULL,
  `overtimeLossPointInUse` tinyint DEFAULT NULL,
  `powerPlayStatsTracked` tinyint DEFAULT NULL,
  `shootingStatsTracked` tinyint DEFAULT NULL,
  `faceoffStatsTracked` tinyint DEFAULT NULL,
  `saveStatsTracked` tinyint DEFAULT NULL,
  PRIMARY KEY (`id`),
  UNIQUE KEY `ID_UNIQUE` (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

