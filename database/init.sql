CREATE DATABASE IF NOT EXISTS hockey_data_hub;

USE hockey_data_hub;

CREATE TABLE IF NOT EXISTS `games` (
  `id` int unsigned NOT NULL,
  `season` int NOT NULL,
  `type` varchar(45) NOT NULL,
  `dateTime` varchar(255) NOT NULL,
  `lastPeriod` varchar(45) NOT NULL,
  `status` varchar(45) NOT NULL,
  `venue` varchar(255) DEFAULT NULL,
  `playoffRound` varchar(255) DEFAULT NULL,
  `playoffSeriesId` int DEFAULT NULL,
  `playoffGameNumber` varchar(45) DEFAULT NULL,
  `awayId` int NOT NULL,
  `awayName` varchar(45) NOT NULL,
  `awayShortName` varchar(45) DEFAULT NULL,
  `awayAbbreviation` varchar(45) NOT NULL,
  `awayLogoURL` mediumtext NOT NULL,
  `awayGoals` int NOT NULL,
  `awayGoalScorers` mediumtext NOT NULL,
  `homeId` int NOT NULL,
  `homeName` varchar(45) NOT NULL,
  `homeShortName` varchar(45) DEFAULT NULL,
  `homeAbbreviation` varchar(45) NOT NULL,
  `homeLogoURL` mediumtext NOT NULL,
  `homeGoals` int NOT NULL,
  `homeGoalScorers` mediumtext NOT NULL,
  PRIMARY KEY (`id`),
  UNIQUE KEY `ID_UNIQUE` (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

