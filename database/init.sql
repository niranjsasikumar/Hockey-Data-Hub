  `teamLogoURL` mediumtext,
  `number` varchar(45) DEFAULT NULL,
  `captain` tinyint DEFAULT NULL,
  `alternateCaptain` tinyint DEFAULT NULL,
  `catches` varchar(45) DEFAULT NULL,
  `nationality` varchar(45) DEFAULT NULL,
  `dateOfBirth` varchar(45) DEFAULT NULL,
  `placeOfBirth` varchar(255) DEFAULT NULL,
  `height` varchar(45) DEFAULT NULL,
  `weight` int DEFAULT NULL,
  `gamesPlayed` int DEFAULT NULL,
  `gamesStarted` int DEFAULT NULL,
  `wins` int DEFAULT NULL,
  `shotsAgainst` int DEFAULT NULL,
  `goalsAgainst` int DEFAULT NULL,
  `saves` int DEFAULT NULL,
  `savePercentage` float DEFAULT NULL,
  `goalsAgainstAverage` float DEFAULT NULL,
  `shutouts` int DEFAULT NULL,
  `timeOnIce` varchar(45) DEFAULT NULL,
  PRIMARY KEY (`id`),
  UNIQUE KEY `ID_UNIQUE` (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

